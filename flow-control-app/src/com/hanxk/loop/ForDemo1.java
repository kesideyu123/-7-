package com.hanxk.loop;

public class ForDemo1 {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++){
            System.out.println("Hello World");
            System.out.println(i);
        }
        for (int i = 1; i <= 10; i += 2){
            System.out.println("Hello World3");
        }
    }
}
